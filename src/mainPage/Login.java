    public void valid(){
        
        String email = txtEmail.getText();
        String pass = String.valueOf(txtPassword.getPassword());
        String nam = txtName.getText();
        String role = cbxRole.getSelectedItem().toString();
        if(!"".equals(email)||!"".equals(pass)||!"".equals(nam)){
           lg.setName(nam);
           lg.setEmail(email);
           lg.setPass(pass);
           lg.setRole(role);
           login.Register(lg);
           Registration start = new Registration();
           start.setVisible(true);
           
          
        }
    }

